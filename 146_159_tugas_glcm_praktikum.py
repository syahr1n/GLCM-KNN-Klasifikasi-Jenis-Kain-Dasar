"""**Path Dataset dan Parameter**"""

dataset_path = '/content/dataset_kain'
output_path = '/content/preprocessed_kain_clahe'
os.makedirs(output_path, exist_ok=True)

# Daftar nama kelas
classes = ['denim', 'katun', 'polyester', 'satin', 'wool']

